package Module8;
import static java.lang.Math.*;
public class StaticImport {

	public static void main(String[] args) {
		System.out.println(pow(7,4));
		System.out.println(pow(4,8));
		System.out.println(pow(9,4));

		System.out.println(random());
		System.out.println(random());
		
		System.out.println(round(4.5));
		System.out.println(round(3.4));
		System.out.println(round(5.5));
		
		System.out.println(random());
	}

}
