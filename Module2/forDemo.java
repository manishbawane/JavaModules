package Module2;

public class forDemo {

	public static void main(String[] args) {
		//Display the name 10 times
		
		for(int i=0;i<=10;i++)
		{
			System.out.println(i + "manish");
		}
		
		int a=1;
		while(a<=10)
		{
			System.out.println(a);
			a++;
		}
		System.out.println("===========================");

		int b=1;
		do {
			System.out.println(b);
			b++;
		}while(b<=10);
	}

	
}
